// Helper to find event by slug
export const getEventBySlug = slug =>
  eventsData.find(event => event.slug === slug);

// Helper to format date
export const formatDate = dateString => {
  const date = new Date(dateString);
  return date.toLocaleDateString('en-US', {
    year: 'numeric',
    month: 'long',
    day: 'numeric',
  });
};

// Helper to get category color
export const getCategoryColor = category => {
  switch (category) {
    case 'Competition':
      return { bg: 'rgba(234, 179, 8, 0.15)', text: '#EAB308', border: 'rgba(234, 179, 8, 0.3)' };
    case 'Workshop':
      return { bg: 'rgba(168, 85, 247, 0.15)', text: '#A855F7', border: 'rgba(168, 85, 247, 0.3)' };
    case 'Networking':
      return { bg: 'rgba(5, 177, 222, 0.15)', text: '#05B1DE', border: 'rgba(5, 177, 222, 0.3)' };
    case 'Event':
      return { bg: 'rgba(34, 197, 94, 0.15)', text: '#22C55E', border: 'rgba(34, 197, 94, 0.3)' };
    default:
      return { bg: 'rgba(5, 177, 222, 0.15)', text: '#05B1DE', border: 'rgba(5, 177, 222, 0.3)' };
  }
};
